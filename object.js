// object literal adalah sebuah object yang berisi key value
// key berfungsi untuk menandai/penanda untuk sebuah data
// sementara value adalah isi dari kunci yang telah di definisikan
// contoh :
const data = {
  nama: "rendi saputra",
  hoby: "game",
};

// cara memanggil data dari sebuah key object literal
// cara memanggil object ada 2 cara
// contoh
console.log(data.nama);
console.log(data["hoby"]);
